<p>
Ristretto is traditionally a short shot of espresso coffee made with the normal amount of ground coffee but extracted with about half the amount of water in the same amount of time by using a finer grind. This produces a concentrated shot of coffee per volume. Just pulling a normal shot short will produce a weaker shot and is not a Ristretto as some believe.[1] The opposite of a ristretto is a lungo, which is typically double the shot volume. Ristretto means “limited” or “restricted” in Italian whereas lungo means “long.” The French equivalent of ristretto is café serré.
